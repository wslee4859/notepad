--음료 전자결재만 조회

	WITH T_FD AS
	(
		SELECT 
			A.CLASS_CODE,
			A.FOLDER_ID,
			A.FOLDER_NAME,
			A.FORM_ID
		FROM [WF].[FOLDER] AS A 
		WHERE A.FOLDER_ID in ('772')   --상위부서 입력


		UNION ALL
		SELECT 
			A.CLASS_CODE,
			A.FOLDER_ID,
			A.FOLDER_NAME,
			A.FORM_ID
		FROM [WF].[FOLDER] AS A 
		INNER JOIN	T_FD B 
		ON A.PARENT_FOLDER_ID = B.FOLDER_ID		    
	)
select FI.FORM_ID
from T_FD AS FD
LEFT JOIN [WF].[WF_FORM_INFORM] AS FI
ON FD.FORM_ID = FI.FORM_ID AND FIELD_NAME = 'COMPANY_MOTTO'
WHERE FI.FORM_ID IS NOT NULL

