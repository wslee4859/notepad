
/*******************************************
* 음료 메일 사용가능 직무에 포함된 인원 조회
* - 음료 LCWare 사용자 중 IM 메일직무에 포함된 인원만 메일 표시 해서 조회
********************************************/
select U.user_name, u.group_name, org.email from [IM].[VIEW_USER] AS U
left join IM.VIEW_ORG AS O
on U.group_code = O.group_code
left join [10.103.1.108].im80.[dbo].[org_user] AS ORG
on U.user_code = ORG.user_id AND ORG.user_id in (select user_id from [10.103.1.108].im80.[dbo].[org_rule_user] where rule_group_id = '8215')
WHERE (U.display_yn = 'Y' OR U.display_yn is null)
AND U.domain_code = '11'
AND org.email is not null
order by O.seq, U.classpos_seq



/*******************************************
* LCWare 사용 계정 음료 (유통사업본부 중에 E로 들어오는 사용자 있음)
********************************************/
select org.sec_level, u.user_code,  U.user_name, u.group_name, org.email AS [IM mail] 
from [IM].[VIEW_USER] AS U
left join IM.VIEW_ORG AS O
on U.group_code = O.group_code
left join [10.103.1.108].im80.[dbo].[org_user] AS ORG
on U.user_code = ORG.user_id 
WHERE (U.display_yn = 'Y' OR U.display_yn is null)
AND U.domain_code = '11'
AND org.sec_level = '9'
order by O.seq, U.classpos_seq


/*******************************************
* LCWare 사용 계정 음료 (메일 사용가능 인원- IM 메일사용자 그룹 기준)
********************************************/
select org.sec_level, u.user_code, U.user_name, u.group_name, u.responsibility_name, u.classpos_name, u.position_name, org_mail.email 
from [IM].[VIEW_USER] AS U
left join IM.VIEW_ORG AS O
on U.group_code = O.group_code
left join [10.103.1.108].im80.[dbo].[org_user] AS ORG
on U.user_code = ORG.user_id 
inner join [10.103.1.108].im80.[dbo].[org_user] AS ORG_mail
on U.user_code = ORG_mail.user_id AND ORG_mail.user_id in (select user_id from [10.103.1.108].im80.[dbo].[org_rule_user] where rule_group_id = '8215') -- IM 메일 사용자기준
WHERE (U.display_yn = 'Y' OR U.display_yn is null)
AND U.domain_code = '11' 
AND org.sec_level = '9' 
order by O.seq, U.classpos_seq











































































































































































































































































































































































































































































































































































































































































































































































































































































































































































